# Creating an empty set using set() constructor
empty_set = set()

# Printing the type to confirm it's a set
print(type(empty_set))  # Output: <class 'set'>

# Creating a set with duplicate values
set = {1, 2, 3, 3, 4}

# Printing the set
# Duplicates are automatically removed in a set
print(set)  # Output: {1, 2, 3, 4}
